# The passed letter is a vowel or consonant
str1 = "Python"
for char in str1:
    if char == 'a' or char == 'e' or char == 'i' or char == 'o' or char == 'u':
        print(f"The character {char} is a vowel")
    else:
        print(f"The character {char} is not a vowel")

